-- Tables used by the Flask routes in backend/routes/*.py
-- Kept separate from the existing public.invoices schema to avoid collisions.

SET statement_timeout = 0;
SET lock_timeout = 0;
SET idle_in_transaction_session_timeout = 0;
SET client_encoding = 'UTF8';
SET standard_conforming_strings = on;

CREATE EXTENSION IF NOT EXISTS "pgcrypto" WITH SCHEMA "extensions";

-- Groups registered via POST /v1/auth/register
CREATE TABLE IF NOT EXISTS public.api_groups (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  group_name text NOT NULL UNIQUE,
  api_token text NOT NULL UNIQUE,
  created_at timestamptz NOT NULL DEFAULT now()
);

-- Invoice templates referenced by templateInvoice
CREATE TABLE IF NOT EXISTS public.api_templates (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  template_id text NOT NULL,
  owner_token text NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT api_templates_owner_template_unique UNIQUE (owner_token, template_id)
);

-- Invoices created via POST /v1/invoices/generate
CREATE TABLE IF NOT EXISTS public.api_invoices (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  owner_token text NOT NULL,
  template_id text NOT NULL,
  xml text NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now()
);

-- For this backend, requests are authenticated at the Flask layer.
-- Disable RLS so the anon key works in local dev. If you switch to service_role
-- you can keep RLS enabled (service role bypasses it).
ALTER TABLE public.api_groups DISABLE ROW LEVEL SECURITY;
ALTER TABLE public.api_templates DISABLE ROW LEVEL SECURITY;
ALTER TABLE public.api_invoices DISABLE ROW LEVEL SECURITY;

